CREATE TABLE Territorio (
id_territorio VARCHAR(60),
provincia VARCHAR(30),
canton VARCHAR(30),
distrito VARCHAR(30),
-- Llave primaria
PRIMARY KEY (id_territorio)
)

CREATE TABLE Proveedor (
id_proveedor VARCHAR(60),
tipo_cedula VARCHAR(60) CHECK (tipo_cedula IN ('Juridica','Fisica')),
nombre VARCHAR(30) NOT NULL,
correo_electronico VARCHAR(80) CHECK (correo_electronico LIKE '%@%.com'),
numero_telefono VARCHAR(20),
-- Llave primaria
PRIMARY KEY (id_proveedor)
)

CREATE TABLE Categoria (
id_categoria INT NOT NULL,
nombre_categoria VARCHAR(30),
-- Llave primaria
PRIMARY KEY (id_categoria)
)

CREATE TABLE Sub_Categoria (
id_subcategoria INT NOT NULL,
id_categoria INT NOT NULL,
nombre_subcategoria VARCHAR(30),
PRIMARY KEY (id_subcategoria),
FOREIGN KEY (id_categoria) REFERENCES Categoria(id_categoria)
)

CREATE TABLE Producto (
id_universal VARCHAR(60),
id_subcategoria INT NOT NULL,
nombre_producto VARCHAR(60),
precio INT NOT NULL,
color VARCHAR(20),
tamano INT,
-- Llave primaria
PRIMARY KEY (id_universal),
-- Llave foranea 
FOREIGN KEY (id_subcategoria) REFERENCES Sub_Categoria(id_subcategoria)
)

CREATE TABLE Cliente (
id_cliente VARCHAR(60),
tipo_cedula VARCHAR(200) CHECK (tipo_cedula IN ('Juridica','Fisica')),
nombre VARCHAR(60),
correo_electronico VARCHAR(200) CHECK (correo_electronico LIKE '%@%.com'),
numero_telefono VARCHAR(20),
PRIMARY KEY (id_cliente)
)

CREATE TABLE Factura (
numero_factura VARCHAR(60),
id_cliente VARCHAR(60),
unidades_compradas INT NOT NULL,
precio INT NOT NULL,
descuento DECIMAL(2,2) NOT NULL,
impuesto DECIMAL(2,2) NOT NULL,
fecha_venta DATE,
PRIMARY KEY (numero_factura),
FOREIGN KEY (id_cliente) REFERENCES Cliente(id_cliente)
)

CREATE TABLE Proveedor_Territorio (
id_ter INT(1) AUTO_INCREMENT,
-- Llave primaria
PRIMARY KEY (id_ter),
-- Llaves foreanas
id_territorio VARCHAR(60),
id_proveedor VARCHAR(60),
FOREIGN KEY (id_territorio) REFERENCES Territorio(id_territorio),
FOREIGN KEY (id_proveedor) REFERENCES Proveedor(id_proveedor)
)

CREATE TABLE Cliente_Territorio (
id_ter INT(1) AUTO_INCREMENT,
-- Llaves foreanas
id_territorio VARCHAR(60),
id_cliente VARCHAR(60),
FOREIGN KEY (id_territorio) REFERENCES Territorio(id_territorio),
FOREIGN KEY (id_cliente) REFERENCES Cliente(id_cliente),
-- Llave primaria
PRIMARY KEY (id_ter)
)

CREATE TABLE Intermedia_Prov_Prod (
id_provprod INT(1) AUTO_INCREMENT,
-- Llaves foreanas
id_proveedor VARCHAR(60),
id_universal VARCHAR(60),
FOREIGN KEY (id_proveedor) REFERENCES Proveedor(id_proveedor),
FOREIGN KEY (id_universal) REFERENCES Producto(id_universal),
-- Llave primaria
PRIMARY KEY (id_provprod)
)

CREATE TABLE Intermedia_Prod_Fac (
id_prodfac INT(1) AUTO_INCREMENT,
-- Llaves foreanas
numero_factura VARCHAR(60),
id_universal VARCHAR(60),
FOREIGN KEY (numero_factura) REFERENCES Factura(numero_factura),
FOREIGN KEY (id_universal) REFERENCES Producto(id_universal),
-- Llave primaria
PRIMARY KEY (id_prodfac)

